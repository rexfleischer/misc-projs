package com.fledwar.util;

/**
 *
 * @author REx
 */
public class PoolManagerException extends Exception
{

    public PoolManagerException()
    {
    }

    public PoolManagerException(String message)
    {
        super(message);
    }

    public PoolManagerException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public PoolManagerException(Throwable cause)
    {
        super(cause);
    }
    
}
