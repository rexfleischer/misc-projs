package com.fledwar.groovy;

/**
 *
 * @author REx
 */
public class GroovyWrapperException extends Exception
{

    public GroovyWrapperException()
    {
    }

    public GroovyWrapperException(String message)
    {
        super(message);
    }

    public GroovyWrapperException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public GroovyWrapperException(Throwable cause)
    {
        super(cause);
    }
    
}
