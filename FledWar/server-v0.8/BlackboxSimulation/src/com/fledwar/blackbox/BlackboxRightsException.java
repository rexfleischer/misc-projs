package com.fledwar.blackbox;

/**
 *
 * @author REx
 */
public class BlackboxRightsException extends Exception
{

    public BlackboxRightsException()
    {
    }

    public BlackboxRightsException(String message)
    {
        super(message);
    }

    public BlackboxRightsException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public BlackboxRightsException(Throwable cause)
    {
        super(cause);
    }
    
}
